num1 = 5
num2 = -2

def suma (numero1, numero2):
    totalSum = numero1 + numero2
    return totalSum

def resta (numero1, numero2):
    totalRes = numero1 + numero2
    return totalRes

resultadoSum = suma(num1, num2)

resultadoRes = resta(num1, num2)

print ('El resultado de la suma es: ', resultadoSum, 'y el resultado de la resta es: ', resultadoRes)